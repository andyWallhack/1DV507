package ao222vn_assign1.ferry;

public class Passenger {
	
	private int passenger;
	private int cost;
	public Passenger() {
		this.passenger = 1;
		this.cost = 20;
	}
	
	/**
	 * 	
	 * @return cost
	 */
	public int getCost() {
		return this.cost;
	}
	
	/**
	 * 
	 * @return passenger
	 */
	public int getPassenger() {
		return this.passenger;
	}

}
